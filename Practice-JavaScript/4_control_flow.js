// Step 4: Control Flow Practice

let num = 10;

// if-else
if (num > 0) {
  console.log("Positive number");
} else if (num < 0) {
  console.log("Negative number");
} else {
  console.log("Zero");
}

// switch
let fruit = "apple";
switch(fruit) {
  case "apple":
    console.log("You chose apple");
    break;
  case "banana":
    console.log("You chose banana");
    break;
  default:
    console.log("Unknown fruit");
}

