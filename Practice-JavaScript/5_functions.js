// Step 5: Functions Practice

// Function Declaration
function greet(name) {
  return `Hello, ${name}`;
}
console.log(greet("John"));

// Function Expression
const square = function(num) {
  return num * num;
};
console.log(square(4));

// Arrow Function
const add = (a, b) => a + b;
console.log(add(5, 3));

